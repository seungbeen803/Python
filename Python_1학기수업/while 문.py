# while 문
# 형식 while 조건식:
value = 5
while value > 0:
    print(value)
    value -= 1

print()

x = 3
while x < 6:
    print(x)
    x += 1

print()

echo = input()

print()

while echo != "exit":
    print(echo)
    echo = input()

echo = input()

print()

while True:
    if echo == "exit":
        break
    print(echo)
    echo = input()